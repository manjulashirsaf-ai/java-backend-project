package com.example;

public class Calculator {

	static int add(int a, int b) {
		
		return a+b;
	}
	
	static int subtract(int a, int b) {
		
		return a-b;
	}
	
	static int multiply(int a, int b) {
		
		return a*b;
	}
	
	static int div(int a, int b) {
		
		return a/b;
	}
	
	static double add(double a, double b) {
		
		return a+b;
	}
	
	static int mod(int a, int b) {
		
		return a%b;
	}
}
